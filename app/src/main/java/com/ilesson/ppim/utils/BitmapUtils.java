    public static final int DEFAULT_WIDTH = 1080;
    public static final int DEFAULT_HEIGHT = 1920;
    public static Bitmap getSmallBitmap(String filePath) {
        final BitmapFactory.Options options = new BitmapFactory.Options();
        options.inJustDecodeBounds = true;
        BitmapFactory.decodeFile(filePath, options);
        int width = options.outWidth;
        int height = options.outHeight;
        if(options.outWidth>options.outHeight){
            height = options.outWidth;
            width = options.outHeight;
        }
        int reqWidth = width;
        int reqHeight = height;
        double scale=1;
        if(width>DEFAULT_WIDTH){
            reqWidth = DEFAULT_WIDTH;
            scale = (double)DEFAULT_WIDTH / (double)width;
            reqHeight = (int)(height*scale);
        }
        Log.d(TAG, "getSmallBitmap: outWidth"+options.outWidth);
        Log.d(TAG, "getSmallBitmap: outHeight"+options.outHeight);
        Log.d(TAG, "getSmallBitmap: reqWidth"+reqWidth);
        Log.d(TAG, "getSmallBitmap: reqHeight"+reqHeight);
        // Calculate inSampleSize
//        options.inSampleSize = 4;
        options.inSampleSize = (int)((double)1/scale);

        Log.d(TAG, "getSmallBitmap: inSampleSize="+options.inSampleSize);
        // Decode bitmap with inSampleSize set
        options.inJustDecodeBounds = false;
        return BitmapFactory.decodeFile(filePath, options);
    }
    /**
     * 得到bitmap的大小
     */
    public static int getBitmapSize(Bitmap bitmap) {
        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.KITKAT) {    //API 19
            return bitmap.getAllocationByteCount();
        }
        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.HONEYCOMB_MR1) {//API 12
            return bitmap.getByteCount();
        }
        // 在低版本中用一行的字节x高度
        return bitmap.getRowBytes() * bitmap.getHeight();                //earlier version
    }
    public static boolean needScale(String filePath){
        final BitmapFactory.Options options = new BitmapFactory.Options();
        options.inJustDecodeBounds = true;
        BitmapFactory.decodeFile(filePath, options);
        if(options.outWidth>DEFAULT_WIDTH){
            return true;
        }
        if(options.outHeight>DEFAULT_HEIGHT){
            return true;
        }
        return false;
    }

